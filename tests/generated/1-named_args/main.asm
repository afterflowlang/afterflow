    mov qword [r12+40], 0 ; store num_remaining
    mov rax, r12 ; copy _7_foo closure env_end to rax
    mov [rbp-16], rax ; store value
    mov rax, [rbp-8] ; load operand
    push rax ; stack arg
    pop rdi ; restore arg into register
