    mov rax, [rbp-16] ; load operand
    mov rbx, 0 ; operand literal
    cmp rax, rbx
    jg foo_release_skip_0
    mov rax, [r12-8] ; load foo_release_field_0 env field
    mov [rbp-24], rax ; store value
